CREATE TABLE comments_votes (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user INTEGER REFERENCES users(id) ON DELETE CASCADE NOT NULL,
    comment INTEGER REFERENCES comments(id) ON DELETE CASCADE NOT NULL,
    status BOOLEAN DEFAULT false,
    up_vote NUMERIC DEFAULT 0,
    down_vote NUMERIC DEFAULT 0
)

CREATE TABLE ideas_votes (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user INTEGER REFERENCES users(id) ON DELETE CASCADE NOT NULL,
    ideas_votes INTEGER REFERENCES ideas(id) ON DELETE CASCADE NOT NULL,
    status BOOLEAN DEFAULT false,
    up_vote NUMERIC DEFAULT 0,
    down_vote NUMERIC DEFAULT 0
)